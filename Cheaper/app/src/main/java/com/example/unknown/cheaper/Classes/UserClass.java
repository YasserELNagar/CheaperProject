package com.example.unknown.cheaper.Classes;

public class UserClass {

    public int id;
    String Name;
    String UserName;
    String Email;
    String Password;


    public UserClass() {
    }

    public UserClass(int id, String name, String userName, String email, String password) {
        this.id = id;
        Name = name;
        UserName = userName;
        Email = email;
        Password = password;
    }
}
